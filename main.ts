game.onUpdate(function () {
    if (myEnemySoldier.left > myPawnBottom.left && enemyDirection || myEnemySoldier.right < myPawnBottom.right && !(enemyDirection)) {
        timer.throttle("enemyChangeDirection", enemyShieldReposition.length * 100, function () {
            enemyDirection = !(enemyDirection)
            changeEnemyDirection()
        })
    }
    if (enemyAttackInProgress) {
        if (enemyDirection) {
            myEnemySoldier.left = myEnemyPosition
        } else {
            myEnemySoldier.right = myEnemyPosition
        }
    }
    myPawnLegs.bottom = myPawnBottom.y
    myPawnLegs.x = myPawnBottom.x
    if (isFiring) {
        upperBodyPlacementX = 8
        if (!(playerDirection)) {
            upperBodyPlacementX = upperBodyPlacementX * -1
        }
        myPawnUpperBody.setPosition(myPawnLegs.x + upperBodyPlacementX, myPawnLegs.y + -17)
    } else {
        upperBodyPlacementX = 5
        if (!(playerDirection)) {
            upperBodyPlacementX = upperBodyPlacementX * -1
        }
        myPawnUpperBody.setPosition(myPawnLegs.x + upperBodyPlacementX, myPawnLegs.top + -7)
    }
    if (isParachuting) {
        myPawnUpperBody.setPosition(myPawnLegs.x + -1, myPawnLegs.top + -8)
        myPawnParachute.setPosition(myPawnUpperBody.x + 0, myPawnUpperBody.y + -21)
        if (myPawnBottom.isHittingTile(CollisionDirection.Bottom)) {
            isParachuting = false
            myPawnParachute.destroy(effects.disintegrate, 500)
            myPawnBottom.ay = 200
            myPawnLegs.setImage(pawnLegsIdle)
            setUpperBodyIdle()
        }
    }
    if (myPawnBottom.isHittingTile(CollisionDirection.Bottom)) {
        if (controller.left.isPressed() || controller.right.isPressed()) {
            if (!(isRunning)) {
                isRunning = true
                animation.runImageAnimation(
                myPawnLegs,
                pawnLegsRunning,
                100,
                true
                )
            }
        } else {
            if (isRunning || isJumping) {
                isRunning = false
                isJumping = false
                animation.stopAnimation(animation.AnimationTypes.All, myPawnLegs)
                myPawnLegs.setImage(pawnLegsIdle)
            }
        }
    }
    scene.centerCameraAt(myPawnBottom.x + 55, myPawnBottom.y + 10)
})
