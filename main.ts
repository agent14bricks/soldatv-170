function changePawnDirection () {
    pawnLegsIdle.flipX()
    pawnShootStance.flipX()
    for (let picture of pawnUpperBodyIdle) {
        picture.flipX()
    }
    for (let picture2 of pawnThrow) {
        picture2.flipX()
    }
    for (let picture3 of pawnLegsJump) {
        picture3.flipX()
    }
    for (let picture4 of pawnLegsRunning) {
        picture4.flipX()
    }
    for (let picture5 of pawnLegsVerticalJump) {
        picture5.flipX()
    }
    for (let picture6 of pawnDying) {
        picture6.flipX()
    }
}
controller.up.onEvent(ControllerButtonEvent.Pressed, function () {
    if (myPawnBottom.isHittingTile(CollisionDirection.Bottom)) {
        myPawnBottom.vy = -150
        isRunning = false
        isJumping = true
        if (controller.left.isPressed() || controller.right.isPressed()) {
            animation.runImageAnimation(
            myPawnLegs,
            pawnLegsJump,
            200,
            false
            )
        } else {
            animation.runImageAnimation(
            myPawnLegs,
            pawnLegsVerticalJump,
            100,
            false
            )
            timer.after(600, function () {
                pawnLegsVerticalJump.reverse()
                animation.runImageAnimation(
                myPawnLegs,
                pawnLegsVerticalJump,
                100,
                false
                )
                timer.after(600, function () {
                    pawnLegsVerticalJump.reverse()
                })
            })
        }
    }
})
