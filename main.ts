function doEnemyAttack (certainEnemy: Sprite) {
    if (enemyDirection) {
        myEnemyPosition = myEnemySoldier.left
    } else {
        myEnemyPosition = myEnemySoldier.right
    }
    animation.runImageAnimation(
    certainEnemy,
    enemySwordAttack,
    100,
    false
    )
    timer.after(800, function () {
        animation.runImageAnimation(
        myPawnLegs,
        pawnDying,
        100,
        false
        )
        myPawnUpperBody.setFlag(SpriteFlag.Invisible, true)
        timer.after(1400, function () {
            enemyAttackInProgress = false
            game.over(false, effects.melt)
        })
    })
}
sprites.onDestroyed(SpriteKind.Enemy, function (sprite) {
    if (sprites.allOfKind(SpriteKind.Enemy).length == 0) {
        myPawnUpperBody.setFlag(SpriteFlag.Invisible, true)
        animation.runImageAnimation(
        myPawnLegs,
        pawnVictory,
        100,
        true
        )
        timer.after(pawnVictory.length * 300, function () {
            animation.runImageAnimation(
            myPawnLegs,
            pawnVictory,
            100,
            true
            )
            timer.after(100, function () {
                game.over(true, effects.confetti)
            })
        })
    }
})
